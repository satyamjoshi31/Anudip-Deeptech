


class Narrow

{

	public static void main(String args[])
	{
		// double to int
		double myd = 10.46;
		int myi = (int) myd;
		System.out.println(myd);
		System.out.println(myi);


		double myd2 = 7.89;
		int myi2 = (int) myd2;
		System.out.println(myd2);
		System.out.println(myi2);
	}


}